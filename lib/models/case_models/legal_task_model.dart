// ─────────────────────────────────────────────
// Priority enum
// ─────────────────────────────────────────────
import 'dart:convert';

enum TaskPriority { high, medium, low }

extension TaskPriorityX on TaskPriority {
  String get label {
    switch (this) {
      case TaskPriority.high:
        return 'High';
      case TaskPriority.medium:
        return 'Medium';
      case TaskPriority.low:
        return 'Low';
    }
  }

  String get apiValue {
    switch (this) {
      case TaskPriority.high:
        return 'high';
      case TaskPriority.medium:
        return 'medium';
      case TaskPriority.low:
        return 'low';
    }
  }

  static TaskPriority fromString(String s) {
    switch (s.toLowerCase()) {
      case 'high':
        return TaskPriority.high;
      case 'low':
        return TaskPriority.low;
      default:
        return TaskPriority.medium;
    }
  }
}

// ─────────────────────────────────────────────
// Bucket enum — matches server-side buckets
// ─────────────────────────────────────────────
enum TaskBucket { overdue, thisWeek, upcoming, noDate, completed }

extension TaskBucketX on TaskBucket {
  String get label {
    switch (this) {
      case TaskBucket.overdue:
        return 'Overdue';
      case TaskBucket.thisWeek:
        return 'This week';
      case TaskBucket.upcoming:
        return 'Upcoming';
      case TaskBucket.noDate:
        return 'No date';
      case TaskBucket.completed:
        return 'Completed';
    }
  }
}

