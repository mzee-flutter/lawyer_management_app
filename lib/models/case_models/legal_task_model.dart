// LegalTaskModel — maps to LegalTaskPublic
// ─────────────────────────────────────────────
class LegalTaskModel {
  final String id;
  final String caseId;
  final String taskTitle;
  final String? notes;
  final TaskPriority priority;
  final DateTime? dueDate;

  final bool isCompleted;
  final DateTime? completedAt;
  final bool isAutoGenerated;
  final String? sourceHearingId;

  final DateTime createdAt;
  final DateTime? updatedAt;

  // Denormalised from Case
  final String caseNumber;
  final String firstPartyName;
  final String? oppositePartyName;
  final String? courtName;

  const LegalTaskModel({
    required this.id,
    required this.caseId,
    required this.taskTitle,
    this.notes,
    required this.priority,
    this.dueDate,
    required this.isCompleted,
    this.completedAt,
    required this.isAutoGenerated,
    this.sourceHearingId,
    required this.createdAt,
    this.updatedAt,
    required this.caseNumber,
    required this.firstPartyName,
    this.oppositePartyName,
    this.courtName,
  });

  factory LegalTaskModel.fromJson(Map<String, dynamic> json) {
    return LegalTaskModel(
      id: json['id'] as String,
      caseId: json['case_id'] as String,
      taskTitle: json['task_title'] as String,
      notes: json['notes'] as String?,
      priority: TaskPriorityX.fromString(json['priority'] as String),
      dueDate: json['due_date'] != null
          ? DateTime.parse(json['due_date'] as String).toLocal()
          : null,
      isCompleted: json['is_completed'] as bool,
      completedAt: json['completed_at'] != null
          ? DateTime.parse(json['completed_at'] as String).toLocal()
          : null,
      isAutoGenerated: json['is_auto_generated'] as bool,
      sourceHearingId: json['source_hearing_id'] as String?,
      createdAt: DateTime.parse(json['created_at'] as String).toLocal(),
      updatedAt: json['updated_at'] != null
          ? DateTime.parse(json['updated_at'] as String).toLocal()
          : null,
      caseNumber: json['case_number'] as String,
      firstPartyName: json['first_party_name'] as String,
      oppositePartyName: json['opposite_party_name'] as String?,
      courtName: json['court_name'] as String?,
    );
  }

  // ── Display helpers ──────────────────────────────────────────

  String get caseTitle {
    if (oppositePartyName != null && oppositePartyName!.isNotEmpty) {
      return '$firstPartyName vs. $oppositePartyName';
    }
    return firstPartyName;
  }

  /// Human-readable due date — "Today", "Tomorrow", "26 Jun", "Overdue 3d"
  String get dueDateLabel {
    if (dueDate == null) return 'No date';
    final now = DateTime.now();
    final today = DateTime(now.year, now.month, now.day);
    final due = DateTime(dueDate!.year, dueDate!.month, dueDate!.day);
    final diff = due.difference(today).inDays;

    if (diff == 0) return 'Due today';
    if (diff == 1) return 'Due tomorrow';
    if (diff == -1) return 'Overdue 1 day';
    if (diff < 0) return 'Overdue ${diff.abs()} days';
    if (diff <= 7) return 'Due in $diff days';

    const months = [
      'Jan',
      'Feb',
      'Mar',
      'Apr',
      'May',
      'Jun',
      'Jul',
      'Aug',
      'Sep',
      'Oct',
      'Nov',
      'Dec'
    ];
    return '${dueDate!.day} ${months[dueDate!.month - 1]}';
  }

  bool get isOverdue {
    if (isCompleted || dueDate == null) return false;
    return dueDate!.isBefore(DateTime.now());
  }

  /// Returns a copy with updated fields (for optimistic UI updates)
  LegalTaskModel copyWith({
    bool? isCompleted,
    DateTime? completedAt,
    String? taskTitle,
    String? notes,
    TaskPriority? priority,
    DateTime? dueDate,
  }) {
    return LegalTaskModel(
      id: id,
      caseId: caseId,
      taskTitle: taskTitle ?? this.taskTitle,
      notes: notes ?? this.notes,
      priority: priority ?? this.priority,
      dueDate: dueDate ?? this.dueDate,
      isCompleted: isCompleted ?? this.isCompleted,
      completedAt: completedAt ?? this.completedAt,
      isAutoGenerated: isAutoGenerated,
      sourceHearingId: sourceHearingId,
      createdAt: createdAt,
      updatedAt: DateTime.now(),
      caseNumber: caseNumber,
      firstPartyName: firstPartyName,
      oppositePartyName: oppositePartyName,
      courtName: courtName,
    );
  }
}

