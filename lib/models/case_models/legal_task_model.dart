class TaskBoardModel {
  final List<LegalTaskModel> overdue;
  final List<LegalTaskModel> thisWeek;
  final List<LegalTaskModel> upcoming;
  final List<LegalTaskModel> noDate;
  final List<LegalTaskModel> completed;

  final int overdueCount;
  final int thisWeekCount;
  final int totalOpen;

  const TaskBoardModel({
    required this.overdue,
    required this.thisWeek,
    required this.upcoming,
    required this.noDate,
    required this.completed,
    required this.overdueCount,
    required this.thisWeekCount,
    required this.totalOpen,
  });

  factory TaskBoardModel.fromJson(Map<String, dynamic> json) {
    List<LegalTaskModel> parse(String key) => (json[key] as List<dynamic>)
        .map((j) => LegalTaskModel.fromJson(j as Map<String, dynamic>))
        .toList();

    return TaskBoardModel(
      overdue: parse('overdue'),
      thisWeek: parse('this_week'),
      upcoming: parse('upcoming'),
      noDate: parse('no_date'),
      completed: parse('completed'),
      overdueCount: json['overdue_count'] as int,
      thisWeekCount: json['this_week_count'] as int,
      totalOpen: json['total_open'] as int,
    );
  }

  /// Returns a new board with a task replaced by its updated version.
  /// Used for optimistic UI updates without a full re-fetch.
  TaskBoardModel withUpdatedTask(LegalTaskModel updated) {
    List<LegalTaskModel> replaceIn(List<LegalTaskModel> list) =>
        list.map((t) => t.id == updated.id ? updated : t).toList();

    return TaskBoardModel(
      overdue: replaceIn(overdue),
      thisWeek: replaceIn(thisWeek),
      upcoming: replaceIn(upcoming),
      noDate: replaceIn(noDate),
      completed: replaceIn(completed),
      overdueCount: overdueCount,
      thisWeekCount: thisWeekCount,
      totalOpen: totalOpen,
    );
  }

  /// Returns a new board with a task removed from all buckets.
  TaskBoardModel withRemovedTask(String taskId) {
    List<LegalTaskModel> removeFrom(List<LegalTaskModel> list) =>
        list.where((t) => t.id != taskId).toList();

    return TaskBoardModel(
      overdue: removeFrom(overdue),
      thisWeek: removeFrom(thisWeek),
      upcoming: removeFrom(upcoming),
      noDate: removeFrom(noDate),
      completed: removeFrom(completed),
      overdueCount: overdueCount,
      thisWeekCount: thisWeekCount,
      totalOpen: totalOpen,
    );
  }

  /// Returns a new board with a new task prepended to the correct bucket.
  TaskBoardModel withNewTask(LegalTaskModel task) {
    final now = DateTime.now();
    final today = DateTime(now.year, now.month, now.day);
    final weekEnd = today.add(const Duration(days: 7));

    if (task.dueDate == null) {
      return copyWithBucket(noDate: [task, ...noDate]);
    }
    final due =
        DateTime(task.dueDate!.year, task.dueDate!.month, task.dueDate!.day);
    if (due.isBefore(today)) {
      return copyWithBucket(overdue: [task, ...overdue]);
    }
    if (due.isBefore(weekEnd)) {
      return copyWithBucket(thisWeek: [task, ...thisWeek]);
    }
    return copyWithBucket(upcoming: [task, ...upcoming]);
  }

  TaskBoardModel copyWithBucket({
    List<LegalTaskModel>? overdue,
    List<LegalTaskModel>? thisWeek,
    List<LegalTaskModel>? upcoming,
    List<LegalTaskModel>? noDate,
    List<LegalTaskModel>? completed,
  }) {
    return TaskBoardModel(
      overdue: overdue ?? this.overdue,
      thisWeek: thisWeek ?? this.thisWeek,
      upcoming: upcoming ?? this.upcoming,
      noDate: noDate ?? this.noDate,
      completed: completed ?? this.completed,
      overdueCount: (overdue ?? this.overdue).length,
      thisWeekCount: (thisWeek ?? this.thisWeek).length,
      totalOpen: (overdue ?? this.overdue).length +
          (thisWeek ?? this.thisWeek).length +
          (upcoming ?? this.upcoming).length +
          (noDate ?? this.noDate).length,
    );
  }
}

